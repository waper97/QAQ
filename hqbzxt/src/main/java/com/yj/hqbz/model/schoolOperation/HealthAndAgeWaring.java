package com.yj.hqbz.model.schoolOperation;

/**
 * 健康证、年龄到期统计
 */
public class HealthAndAgeWaring {
    //总数
    private Integer total;
    //一个月健康证到期预警
    private Integer oneMaturity;
    //二个月健康证到期预警
    private Integer twoMaturity;
    //差一个月年龄预警
    private  Integer oneAge;
    //差二个月年龄预警
    private  Integer twoAge;

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getOneMaturity() {
        return oneMaturity;
    }

    public void setOneMaturity(Integer oneMaturity) {
        this.oneMaturity = oneMaturity;
    }

    public Integer getTwoMaturity() {
        return twoMaturity;
    }

    public void setTwoMaturity(Integer twoMaturity) {
        this.twoMaturity = twoMaturity;
    }

    public Integer getOneAge() {
        return oneAge;
    }

    public void setOneAge(Integer oneAge) {
        this.oneAge = oneAge;
    }

    public Integer getTwoAge() {
        return twoAge;
    }

    public void setTwoAge(Integer twoAge) {
        this.twoAge = twoAge;
    }
}
